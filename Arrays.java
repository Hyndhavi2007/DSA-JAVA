
public class Arrays {
    public static void main(String[] args) {
     
        int[] marks = new int[6];
        System.out.println("Enter the marks of 6 subjects:");
       marks[0]=99;//Maths marks
       marks[1] = 97;//Physics marks
       marks[2] = 95;//Chemistry marks
       marks[3] = 98;//Biology marks
       marks[4] = 96;//English marks
       marks[5] = 94;//Computer Science marks

     // loop
        for (int i = 0; i<=5;i++){
            System.out.println(marks[i]);
        }
       


     
    }
}
